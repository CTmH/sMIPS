.org 0x0
.set noat
.global _start

_start:
lui $1, 0x0000
lui $2, 0xffff
lui $3, 0x0505
lui $4, 0x0000
#对于movz指令而言，由于寄存器$1为0，所以将$2的值赋给$4
movz $4, $2, $1
#对于movn指令而言，由于寄存器$1为0，所以不赋值，$4保持不变
movn $4, $3, $1
#对于movn指令而言，由于寄存器$2不为0，所以将$3的值赋给$4
movn $4, $3, $2
#对于movz指令而言，由于寄存器$3不为0，所以不赋值，$4保持不变
movz $4, $2, $3


